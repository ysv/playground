package lo

// Reverse is modified reverse from https://github.com/samber/lo
func Reverse[T any](collection []T) []T {
	length := len(collection)
	half := length / 2

	for i := 0; i < half; i = i + 1 {
		j := length - 1 - i
		collection[i], collection[j] = collection[j], collection[i]
	}

	return append(collection, collection[0])
}
